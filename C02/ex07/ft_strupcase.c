char	*ft_strupcase(char *str)
{
	int	a;

	a = 0;
	while (str[a])
	{
		if (str[a] >= 'a' && str[a] <= 'z')
			str[a] = str[a] - 32 ;
		a++ ;
	}
	return (str);
}
