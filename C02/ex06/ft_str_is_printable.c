int	ft_str_is_printable(char *str)
{
	int	a;

	a = 0 ;
	while (str[a])
	{
		if (!(str[a] >= 32 && str[a] <= 127)
			|| (str[a] == '\0'))
			return (0);
		a++ ;
	}
	return (1);
}
