int	ft_atoi(char *str)
{
	int	a;
	int	signe;
	int	b;

	a = 0;
	signe = 1;
	while ((str[a] >= 9 && str[a] <= 13) || (str[a] == ' '))
	{
		a++ ;
	}
	while (str[a] == '+' || str[a] == '-')
	{
		if (str[a] == '-')
			signe = signe * -1;
		a++ ;
	}
	b = 0;
	while (str[a] >= '0' && str[a] <= '9')
	{
		b *= 10 ;
		b += str[a] - '0';
		a++ ;
	}
	return (b * signe);
}
