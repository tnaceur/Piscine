int	ft_iterative_power(int nb, int power)
{
	int	i;
	int	j;

	i = 0;
	j = 1;
	if (power < 0)
		return (0);
	else if (power == 0)
		return (1);
	else if (nb == 0 && power == 0)
		return (1);
	else
	{
		while (i < power)
		{
			j *= nb;
			i++ ;
		}
	}
	return (j);
}
